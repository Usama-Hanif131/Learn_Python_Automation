from datetime import datetime  # Import the 'datetime' module from the 'datetime' package
current_time = datetime.now()  # Get the current date and time
print("Current time:", current_time)  # Print the current date and time
print("Day:", current_time.day)  # Print the day of the month from the current date and time
print("Month:", current_time.month)  # Print the month from the current date and time
print("Year:", current_time.year)  # Print the year from the current date and time
